    <div class="modal-dialog" ref="dlg" :style="dialogInlineStyle">
      <div class="modal-content shadow" :style="modalContentStyle">
        <div class="modal-header">
          <h5 class="modal-title">ÏÇ¨Ïö©Ïûê ÏÑ†ÌÉù</h5>
          <button type="button" class="btn-close" aria-label="Close" @click="onClose"></button>
        </div>

        <div class="modal-body">
          <div class="row g-3">
            <!-- LEFT: Ï†ÑÏ≤¥ ÏÇ¨Ïö©Ïûê Î™©Î°ù -->
            <div class="col-12 col-lg-6">
              <div class="border rounded-3">
                <div class="p-2 border-bottom">
                  <input
                    v-model="leftKeyword"
                    class="form-control form-control-sm"
                    placeholder="Í≤ÄÏÉâ(ID/Ïù¥Î¶Ñ/Î∂ÄÏÑú/ÏßÅÎ¨¥)"
                  />
                </div>

                <!-- ÌëúÎäî ÏßÅÏ†ë Î†åÎçî, ÏïÑÎûòÏóê PagerÎßå 'Î∂ôÏûÑ' -->
                <div class="table-responsive">
                  <table class="table table-sm table-hover mb-0 align-middle">
