                            />
                          </div>
                        </th>
                        <th class="text-nowrap">UserId</th>
                        <th>ÏÇ¨Ïö©ÏûêÎ™Ö</th>
                        <th>Î∂ÄÏÑúÎ™Ö</th>
                        <th>ÏßÅÎ¨¥</th>
                      </tr>
                    </thead>
                    <tbody>
                      <!-- ‚úÖ Ïó¨Í∏∞ÏÑú items ÎåÄÏã† paginatedLeft ÏÇ¨Ïö© -->
                      <tr v-for="u in paginatedLeft" :key="u.userId">
                        <td>
                          <div class="form-check m-0 d-flex justify-content-center">
                            <input
