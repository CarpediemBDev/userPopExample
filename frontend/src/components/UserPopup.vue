            </div>

            <!-- RIGHT: ÏÑ†ÌÉù ÎØ∏Î¶¨Î≥¥Í∏∞ -->
            <div class="col-12 col-lg-6">
              <!-- ‚úÖ Ïó¨Í∏∞ÏÑúÎèÑ items ÎåÄÏã† pagedPreview ÏÇ¨Ïö© -->
              <SelectedUsers
                :users="pagedPreview"
                @remove="onRemoveSelected"
                @clear="onClearSelected"
              />

              <!-- üîª ÏÑ†ÌÉùÏòÅÏó≠ÏóêÎèÑ Pager 'Î∂ôÏûÑ' (ÌïÑÏöîÌï† ÎïåÎßå ÌëúÏãú) -->
              <PagedList
                v-if="preview.length > rightPageSize"
